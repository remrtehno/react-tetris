import {cloneArray} from "../cloneArray";

describe("cloneArray", () => {
  it("simple array", () => {
    const clone = cloneArray([1,99,9999999999]);
    expect(clone).toEqual([1,99,9999999999]);
  });
  it("[1,2,4,[12,33,4]]", () => {
    const clone = cloneArray([1,2,4,[12,33,4]]);
    expect(clone).toEqual([1,2,4,[12,33,4]]);
  });
  it("[1,2,4,[12,33,4],[3,[3,[3]]]]", () => {
    const clone = cloneArray([1,2,4,[12,33,4],[3,[3,[3]]]]);
    expect(clone).toEqual([1,2,4,[12,33,4],[3,[3,[3]]]]);
  });
  it("[1,2,4,[12,33,4],[3,[3,[3], ,],987,8888, , ], , ,78655555]", () => {
    const clone = cloneArray([1,2,4,[12,33,4],[3,[3,[3], ,],987,8888, , ], , ,78655555]);
    expect(clone).toEqual([1,2,4,[12,33,4],[3,[3,[3], ,],987,8888, , ], , ,78655555]);
  });

  it("[1,2,4,[12,33,4],[3,[3,[3], ,],987,8888, , ], , ,78655555]", () => {
    const clone = cloneArray([1,2,4,[12,33,4],[3,[3,[3], ,],987,8888, , ], , ,78655555]);
    expect(clone).toEqual([1,2,4,[12,33,4],[3,[3,[3], ,],987,8888, , ], , ,78655555]);
  });
});


